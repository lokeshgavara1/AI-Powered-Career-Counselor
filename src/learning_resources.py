def get_learning_resources(skills):
    """
    Get learning resources for a list of skills.
    Handles case-insensitive matching and common skill variations.
    """
    if not skills:
        return {}
    
    resources = {}
    
    # Create a lowercase mapping for case-insensitive lookup
    skill_lowercase_map = {skill.lower(): skill for skill in SKILL_RESOURCES.keys()}
    
    for skill in skills:
        skill_lower = skill.lower().strip()
        
        # Direct match
        if skill_lower in skill_lowercase_map:
            original_key = skill_lowercase_map[skill_lower]
            resources[skill] = SKILL_RESOURCES[original_key]
            continue
        
        # Handle common variations and aliases
        skill_variations = {
            'js': 'javascript',
            'node.js': 'nodejs',
            'react.js': 'react',
            'vue.js': 'vue',
            'express.js': 'express',
            'next': 'next.js',
            'py': 'python',
            'ml': 'machine learning',
            'ai': 'machine learning',
            'postgres': 'postgresql',
            'mongo': 'mongodb',
            'k8s': 'kubernetes',
            'tf': 'tensorflow',
            'sk-learn': 'scikit-learn',
            'sklearn': 'scikit-learn',
            'cv': 'computer vision',
            'nlp': 'nlp',
            'devops': 'ci/cd',
            'amazon web services': 'aws',
            'google cloud platform': 'gcp',
            'google cloud': 'gcp',
            'microsoft azure': 'azure',
            'power bi': 'powerbi',
            'ms excel': 'excel',
            'microsoft excel': 'excel',
            'jquery': 'jquery',
            'rest api': 'rest',
            'restful': 'rest',
            'graphql': 'graphql',
            'nosql': 'mongodb',
            'rdbms': 'sql',
            'version control': 'git',
            'containerization': 'docker',
            'orchestration': 'kubernetes',
            'infrastructure as code': 'terraform',
            'continuous integration': 'ci/cd',
            'continuous deployment': 'ci/cd',
            'test driven development': 'testing',
            'tdd': 'testing',
            'behavioral driven development': 'testing',
            'bdd': 'testing',
            'large language models': 'llm',
            'large language model': 'llm',
            'openai api': 'openai',
            'chatgpt': 'openai',
            'gpt-3': 'gpt',
            'gpt-4': 'gpt',
            'bert': 'bert',
            'transformer': 'transformers',
            'hugging face': 'huggingface',
        }
        
        # Check variations
        if skill_lower in skill_variations:
            mapped_skill = skill_variations[skill_lower]
            if mapped_skill in skill_lowercase_map:
                original_key = skill_lowercase_map[mapped_skill]
                resources[skill] = SKILL_RESOURCES[original_key]
                continue
        
        # Handle partial matches for compound skills
        for resource_skill in skill_lowercase_map.keys():
            if skill_lower in resource_skill or resource_skill in skill_lower:
                original_key = skill_lowercase_map[resource_skill]
                resources[skill] = SKILL_RESOURCES[original_key]
                break
    
    return resources
